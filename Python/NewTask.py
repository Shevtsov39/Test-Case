   secMax = array[0]
   max = array[0]
   for digit in array :
      if digit < secMin and digit > min :
         secMin = digit
      elif digit < min :
         min,secMin = digit,min
      if digit > secMax and digit < max :
         secMax = digit
      elif digit > max :
         max,secMax = digit,max
   if min*secMax > secMin*max :
      return secMin*max
   else :
      return min*secMax

